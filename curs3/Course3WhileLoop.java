public class Course3WhileLoop{
	public static void main(String[] args){
			learnWhile();
			learnDoWhile();

	}

	public static void learnDoWhile(){
		int a=20;
		do{
			System.out.println(a);
			a--;
		} while(a>10);


	}




	public static void learnWhile(){
		int a = 20;

		while(a>10){
			System.out.println(a);
			a--;


		}

	}

}